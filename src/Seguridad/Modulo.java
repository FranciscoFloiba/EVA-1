    public void habilitarCampos(boolean e)
    {
        modulo.setEditable(e);
        nombre.setEditable(e);
        etiqueta.setEnabled(e);
        estado.setEnabled(e);
        tipo.setEnabled(e);
        sub.setEditable(e);
        subsub.setEditable(e);
        componente.setEnabled(e);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Modulo oModulo = new Datos.Modulo(enlace);
        oModulo.Buscar(idx);
        id.setText(Integer.toString(oModulo.getID())); 
        modulo.setText(Integer.toString(oModulo.getModulo()));
        sub.setText(Integer.toString(oModulo.getSubModulo()));
        subsub.setText(Integer.toString(oModulo.getSubSubModulo()));
        nombre.setText(oModulo.getNombre());
        etiqueta.setSelected(oModulo.getEtiqueta());
        estado.setSelected(oModulo.getEstado().equals("A")?true:false);
        tipo.setSelectedIndex(oModulo.getTipo());             
        componente.setSelectedItem(new ListaEleccionSeguridad(0,"",oModulo.getComponente()));
   } 
    
