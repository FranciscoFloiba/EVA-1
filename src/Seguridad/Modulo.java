    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos        
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el nombre de la Asignatura",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }    
      
      if (componente.getSelectedIndex()==-1)
      {
          JOptionPane.showMessageDialog(this, "No seleccionó un componente para el módulo",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }    
   
    
      Datos.Modulo oModulo = new Datos.Modulo(enlace);    
        
      // El ID lo genera automáticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oModulo.setModulo(idx,
                        Integer.valueOf(modulo.getText()),
                        Integer.valueOf(sub.getText()),
                        Integer.valueOf(subsub.getText()),
                        nombre.getText(),
                        etiqueta.isSelected(),
                        estado.isSelected()?"A":"B",
                        tipo.getSelectedIndex(),
                        ((ListaEleccionSeguridad)componente.getSelectedItem()).componente); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oModulo.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
        else
        {
          padre.cargarTabla();
          nombre.requestFocus();
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oModulo.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oModulo.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      padre.cargarTabla();
      this.dispose();      
    }//GEN-LAST:event_boton_guardarActionPerformed

