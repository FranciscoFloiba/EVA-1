    public void configurarPara(String accion)
    {
        this.accion = accion;
        if (accion.equals("NUEVO"))
        {            
           id.setEnabled(false);
           // Password aleatorio
           password.setText(password.getPassword().toString());
        }

        if (accion.equals("EDITAR"))
        {
            habilitarCampos(true);
            id.setEnabled(false);
        }
        
        if (accion.equals("ELIMINAR"))
        {
            habilitarCampos(false);            
        }
   }
    
   public void habilitarCampos(boolean estado)
   {
        id.setEditable(estado);
        nombre.setEditable(estado);    
        apellido.setEditable(estado);
        telefono.setEditable(estado);
        email1.setEditable(estado);
        email2.setEditable(estado);
        login.setEditable(estado);
        password.setEditable(estado);
        status.setEnabled(estado);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Administrador oAdministrador = new Datos.Administrador(enlace);
        oAdministrador.Buscar(idx);        
        id.setText(Integer.toString(oAdministrador.getID()));        
        nombre.setText(oAdministrador.getNombre());
        apellido.setText(oAdministrador.getApellido());
        telefono.setText(oAdministrador.getTelefono());
        email1.setText(oAdministrador.getEmail1());
        email2.setText(oAdministrador.getEmail2());
        login.setText(oAdministrador.getLogin());
        password.setText(oAdministrador.getPassword());
        status.setSelected(oAdministrador.getStatus().equals("A")?true:false);
   } 
    
