    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos        
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el nombre del administrador",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }    
      
      if (apellido.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el apellido del Administrador",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          apellido.requestFocus();
          return;
      }     
      
      if (login.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el login del Administrador",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          login.requestFocus();
          return;
      }   
      
      if (password.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el password del Administrador",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          password.requestFocus();
          return;
      }   
    
      Datos.Administrador oAdministrador = new Datos.Administrador(enlace);    
        
      // El ID lo genera automáticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oAdministrador.setAdministrador(idx,
                                      nombre.getText(),
                                      apellido.getText(),
                                      telefono.getText(),
                                      login.getText(),
                                      password.getText(),
                                      email1.getText(),
                                      email2.getText(),
                                      (status.isSelected()?"A":"B"));

      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oAdministrador.Nuevo())
        {
          nombre.requestFocus();
          return;
        }       
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oAdministrador.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oAdministrador.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      padre.cargarTabla();
      this.dispose();      
    }//GEN-LAST:event_boton_guardarActionPerformed

