    public void habilitarCampos(boolean estado)
    {
        id.setEnabled(false);
        tema.setEditable(estado);
        sub.setEditable(estado);
        subsub.setEditable(estado);
        nombre.setEditable(estado);    
        conaic_area.setEnabled(estado);
        egel_area.setEnabled(estado);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Asignatura_Tema oAsignatura_Tema = new Datos.Asignatura_Tema(enlace);
        oAsignatura_Tema.Buscar(idx);
        id.setText(Integer.toString(oAsignatura_Tema.getID()));  
        tema.setText(Integer.toString(oAsignatura_Tema.getTema()));
        sub.setText(Integer.toString(oAsignatura_Tema.getSubTema()));
        subsub.setText(Integer.toString(oAsignatura_Tema.getSubSubTema()));
        nombre.setText(oAsignatura_Tema.getNombre());        
        conaic_area.setSelectedItem(new ListaEleccion(oAsignatura_Tema.getConaicArea_id(),""));        
        egel_area.setSelectedItem(new ListaEleccion(oAsignatura_Tema.getEgelArea_id(),""));
        id_carrera = oAsignatura_Tema.getCarrera_id();
   } 
   
   
    
