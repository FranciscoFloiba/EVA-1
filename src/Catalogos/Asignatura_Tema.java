    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos      
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se captur칩 el nombre del tema, subtema o subsubtema",
                                              "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }            
    
      Datos.Asignatura_Tema oAsignatura_Tema = new Datos.Asignatura_Tema(enlace);    
        
      // El ID lo genera autom치ticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oAsignatura_Tema.setAsignatura_Tema(0,
                                Integer.valueOf(tema.getText()),
                                Integer.valueOf(sub.getText()),
                                Integer.valueOf(subsub.getText()),
                                nombre.getText(),
                                id_asignatura,
                                ((ListaEleccion)conaic_area.getSelectedItem()).id,
                                ((ListaEleccion)egel_area.getSelectedItem()).id); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oAsignatura_Tema.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
        else
        {
          padre.cargarTabla();
          nombre.setText("");
          nombre.requestFocus();
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oAsignatura_Tema.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oAsignatura_Tema.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      padre.cargarTabla();
      this.dispose();      
    }//GEN-LAST:event_boton_guardarActionPerformed

