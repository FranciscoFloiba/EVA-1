    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el nombre de la Institución",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }
      
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó la población de la Institución",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          poblacion.requestFocus();
          return;
      }      
           
    
      Datos.Institucion oInstitucion = new Datos.Institucion(enlace);    
        
      // El ID lo genera automáticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oInstitucion.setInstitucion(idx,
                                  nombre.getText(),
                                  poblacion.getText(),
                                  colonia.getText(),
                                  calle_y_numero.getText(),                                  
                                  ((ListaEleccion)municipio.getSelectedItem()).getId(),
                                  ((ListaEleccion)contacto.getSelectedItem()).getId()
                                  ); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oInstitucion.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oInstitucion.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oInstitucion.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      
      this.dispose();
      padre.cargarTabla();
    }//GEN-LAST:event_boton_guardarActionPerformed

