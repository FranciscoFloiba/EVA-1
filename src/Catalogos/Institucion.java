    public void configurarPara(String accion)
    {
        this.accion = accion;
        if (accion.equals("NUEVO"))
        {            
           // limpiarCampos();
           id.setEnabled(false);
        }

        if (accion.equals("EDITAR"))
        {
            habilitarCampos(true);
            id.setEnabled(false);
        }
        
        if (accion.equals("ELIMINAR"))
        {
            habilitarCampos(false);            
        }
         
        
        // Cargamos los datos de la lista de municipios
        Datos.Municipio oMunicipio = new Datos.Municipio(enlace);
        DefaultComboBoxModel datos_municipio = new DefaultComboBoxModel();
        Vector items_municipio = new Vector();
        datos_municipio.removeAllElements();
        items_municipio.removeAllElements();
        
        items_municipio.addAll(oMunicipio.Lista());
        for (int i = 0; i < items_municipio.size(); i++)
        {
          datos_municipio.addElement(items_municipio.get(i));
        }
        municipio.setModel(datos_municipio);    
        
        
        // Cargamos los datos de la lista de contactos
        Datos.Contacto oContacto = new Datos.Contacto(enlace);
        DefaultComboBoxModel datos_contacto = new DefaultComboBoxModel();
        Vector items_contacto = new Vector();
        datos_contacto.removeAllElements();
        items_contacto.removeAllElements();
        items_contacto.addAll(oContacto.Lista());
        for (int i = 0; i < items_contacto.size(); i++)
        {
          datos_contacto.addElement(items_contacto.get(i));
        }
        contacto.setModel(datos_contacto);   
    }
    
    public void habilitarCampos(boolean estado)
    {        
        id.setEditable(estado);
        nombre.setEditable(estado);  
        poblacion.setEditable(estado);  
        colonia.setEditable(estado);
        calle_y_numero.setEditable(estado);
        contacto.setEnabled(estado);
        municipio.setEnabled(estado);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Institucion oInstitucion = new Datos.Institucion(enlace);
        oInstitucion.Buscar(idx);  
        id.setText(Integer.toString(oInstitucion.getID()));        
        nombre.setText(oInstitucion.getNombre());
        poblacion.setText(oInstitucion.getPoblacion());
        colonia.setText(oInstitucion.getColonia());
        calle_y_numero.setText(oInstitucion.getCalle_y_Numero());
        municipio.setSelectedItem(new ListaEleccion(oInstitucion.getMunicipio_Id(),""));
        contacto.setSelectedItem(new ListaEleccion(oInstitucion.getContacto_Id(),""));
   } 
    
