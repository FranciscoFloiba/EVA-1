    public void configurarPara(String accion)
    {
        this.accion = accion;
        if (accion.equals("NUEVO"))
        {            
           id.setEnabled(false);
        }

        if (accion.equals("EDITAR"))
        {
            habilitarCampos(true);
            id.setEnabled(false);
        }
        
        if (accion.equals("ELIMINAR"))
        {
            habilitarCampos(false);            
        }
         
        // Cargamos los datos de las instituciones
        Datos.Institucion oInstitucion = new Datos.Institucion(enlace);
        DefaultComboBoxModel datos_institucion = new DefaultComboBoxModel();
        datos_institucion.removeAllElements();
        Vector items_institucion = new Vector();
        items_institucion.addAll(oInstitucion.Lista());
        for (int i = 0; i < items_institucion.size(); i++)
        {
          datos_institucion.addElement(items_institucion.get(i));
        }
        institucion.setModel(datos_institucion);        
        
        // Cargamos los datos de los egels
        Datos.Egel oEgel = new Datos.Egel(enlace);
        DefaultComboBoxModel datos_egel = new DefaultComboBoxModel();
        datos_egel.removeAllElements();
        Vector items_egel = new Vector();
        items_egel.addAll(oEgel.Lista());
        for (int i = 0; i < items_egel.size(); i++)
        {
          datos_egel.addElement(items_egel.get(i));
        }
        egel.setModel(datos_egel);   
        
        // Cargamos los datos de los contactos
        Datos.Contacto oContacto = new Datos.Contacto(enlace);
        DefaultComboBoxModel datos_contacto = new DefaultComboBoxModel();
        datos_contacto.removeAllElements();
        Vector items_contacto = new Vector();
        items_contacto.addAll(oContacto.Lista());
        for (int i = 0; i < items_contacto.size(); i++)
        {
          datos_contacto.addElement(items_contacto.get(i));
        }
        contacto.setModel(datos_contacto); 
    }
    
    public void habilitarCampos(boolean estado)
    {
        institucion.setEnabled(estado);
        id.setEditable(estado);
        nombre.setEditable(estado);      
        egel.setEnabled(estado);
        contacto.setEnabled(estado);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Carrera oCarrera = new Datos.Carrera(enlace);
        oCarrera.Buscar(idx);  
        institucion.setSelectedItem(new ListaEleccion(oCarrera.getInstitucion_id(),""));
        id.setText(Integer.toString(oCarrera.getID())); 
        nombre.setText(oCarrera.getNombre());
        egel.setSelectedItem(new ListaEleccion(oCarrera.getEgel_id(),""));     
        contacto.setSelectedItem(new ListaEleccion(oCarrera.getContacto_id(),""));
   } 
    
