    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se captur칩 el nombre de la Carrera",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }           
    
      Datos.Carrera oCarrera = new Datos.Carrera(enlace);    
        
      // El ID lo genera autom치ticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oCarrera.setCarrera(idx,
                          nombre.getText(),
                          ((ListaEleccion)egel.getSelectedItem()).getId(),
                          ((ListaEleccion)contacto.getSelectedItem()).getId(),
                          ((ListaEleccion)institucion.getSelectedItem()).getId()); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oCarrera.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oCarrera.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oCarrera.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      
      this.dispose();
      padre.cargarTabla();
    }//GEN-LAST:event_boton_guardarActionPerformed

