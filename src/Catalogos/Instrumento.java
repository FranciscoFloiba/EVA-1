    public void configurarPara(String accion)
    {
        this.accion = accion;
        if (accion.equals("NUEVO"))
        {            
           // limpiarCampos();
           id.setEnabled(false);
        }

        if (accion.equals("EDITAR"))
        {
            habilitarCampos(true);
            id.setEnabled(false);
        }
        
        if (accion.equals("ELIMINAR"))
        {
            habilitarCampos(false);            
        }
         
        // Cargamos los datos de la lista
        Datos.Egel oEgel = new Datos.Egel(enlace);
        DefaultComboBoxModel datos = new DefaultComboBoxModel();
        datos.removeAllElements();
        Vector items = new Vector();
        items.addAll(oEgel.Lista());
        for (int i = 0; i < items.size(); i++)
        {
          datos.addElement(items.get(i));
        }
        egel.setModel(datos);         
    }
    
    public void habilitarCampos(boolean estado)
    {
        egel.setEnabled(estado);
        id.setEditable(estado);
        nombre.setEditable(estado);      
        numero_reactivos.setEditable(estado);
        dificultad.setEnabled(estado);
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Instrumento oInstrumento = new Datos.Instrumento(enlace);
        oInstrumento.Buscar(idx);  
        egel.setSelectedItem(new ListaEleccion(oInstrumento.getIdEgel(),""));
        id.setText(Integer.toString(oInstrumento.getID()));        
        nombre.setText(oInstrumento.getNombre());
        numero_reactivos.setText(String.valueOf(oInstrumento.getNumeroReactivos()));
        dificultad.setSelectedIndex(oInstrumento.getDificultad());
   } 
    
