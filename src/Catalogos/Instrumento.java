    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el nombre del Instrumento",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }
      
      /*if (numero_reactivos.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el número de reactivos del Instrumento",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          numero_reactivos.requestFocus();
          return;
      }*/       
    
      Datos.Instrumento oInstrumento = new Datos.Instrumento(enlace);    
        
      // El ID lo genera automáticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oInstrumento.setInstrumento(idx,
                                  nombre.getText(),
                                  0,
                                  dificultad.getSelectedIndex(),
                                  ((ListaEleccion)egel.getSelectedItem()).id
                                  ); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oInstrumento.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oInstrumento.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oInstrumento.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      
      this.dispose();
      padre.cargarTabla();
    }//GEN-LAST:event_boton_guardarActionPerformed

