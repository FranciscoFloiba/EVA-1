    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
      // Validamos los datos
      if (nombre.getText().isEmpty())
      {
          JOptionPane.showMessageDialog(this, "No se capturó el nombre del Municipio",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
          nombre.requestFocus();
          return;
      }     
          
      Datos.Municipio oMunicipio = new Datos.Municipio(enlace);    
        
      // El ID lo genera automáticamente la base de datos cuando el registro es nuevo
      int idx;
      idx = (id.getText().isEmpty())?0:Integer.valueOf(id.getText());
      oMunicipio.setMunicipio(idx,
                              nombre.getText(),                                  
                              ((ListaEleccion)estado.getSelectedItem()).id
                              ); 
      
      if (this.accion.equals("NUEVO"))
      {
        // Se manda escribir a la base de datos
        if (!oMunicipio.Nuevo())
        {
          nombre.requestFocus();
          return;
        }
        else
        {
          nombre.setText("");
          nombre.requestFocus();
          padre.cargarTabla();
          return;
        }
      }
   
      if (this.accion.equals("EDITAR"))
      {
        // Se manda escribir a la base de datos
        if (!oMunicipio.Editar())
        {
          nombre.requestFocus();
          return;
        }
      }      
    
      if (this.accion.equals("ELIMINAR"))
      {
        // Se manda escribir a la base de datos
        if (!oMunicipio.Eliminar())
        {
          boton_guardar.requestFocus();
          return;
        }
      }
      
      this.dispose();
      padre.cargarTabla();      
    }//GEN-LAST:event_boton_guardarActionPerformed

