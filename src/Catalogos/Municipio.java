    public void configurarPara(String accion)
    {
        this.accion = accion;
        if (accion.equals("NUEVO"))
        {            
           // limpiarCampos();
           id.setEnabled(false);
        }

        if (accion.equals("EDITAR"))
        {
            habilitarCampos(true);
            id.setEnabled(false);
        }
        
        if (accion.equals("ELIMINAR"))
        {
            habilitarCampos(false);            
        }
         
        // Cargamos los datos de la lista
        Datos.Estado oEstado = new Datos.Estado(enlace);
        DefaultComboBoxModel datos = new DefaultComboBoxModel();
        datos.removeAllElements();
        Vector items = new Vector();
        items.addAll(oEstado.Lista());
        for (int i = 0; i < items.size(); i++)
        {
          datos.addElement(items.get(i));
        }
        estado.setModel(datos);         
    }
    
    public void habilitarCampos(boolean estado)
    {
        this.estado.setEnabled(estado);
        id.setEditable(estado);
        nombre.setEditable(estado); 
   }
    
   public void cargarCampos(int idx)
    {
        Datos.Municipio oMunicipio = new Datos.Municipio(enlace);
        oMunicipio.Buscar(idx);  
        estado.setSelectedItem(new ListaEleccion(oMunicipio.getEstado_ID(),""));
        id.setText(Integer.toString(oMunicipio.getID()));        
        nombre.setText(oMunicipio.getNombre());        
   } 
    
