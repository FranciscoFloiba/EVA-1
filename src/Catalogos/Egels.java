    public void cargarTabla()
    {
       // Ejecutar metodo para recuperar los registros de la tabla de Institucion
      Datos.Egel oEgel = new Datos.Egel(database);
      
      try {
          
          if (filtrar) 
          {
            // Validamos en campo de id              
            if (campo.getSelectedIndex()==0)
            {
                try
                {
                   Integer.parseInt(CajaBusqueda.getText());
                }
                catch (NumberFormatException error)
                {
                   JOptionPane.showMessageDialog(this, "Patrón de búsqueda incorrecto, se esperaban números", "Error", JOptionPane.ERROR_MESSAGE);
                   filtrar = false;
                   CajaBusqueda.setText(""); btn_Buscar.doClick();
                   return;     
                }                             
            }            
            registros = oEgel.DataSetEgel(CajaBusqueda.getText(), campo.getSelectedIndex()); 
          }  
          else
          {
            registros = oEgel.DataSetEgel();
          }
      } catch (SQLException ex) {
          Logger.getLogger(Egels.class.getName()).log(Level.SEVERE, null, ex);
      }
       
      // Se crea una manejador para los datos que van a recuperarse
      ModeloTabla modelo = new ModeloTabla();   
      modelo.rs = registros;
 
      try 
        {
         modelo.rsmd=registros.getMetaData();
        } 
      catch (SQLException ex) 
        {
         Logger.getLogger(Egels.class.getName()).log(Level.SEVERE, null, ex);
        }
      
      tabla.setModel(modelo);  
      tabla.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
      tabla.getColumnModel().getColumn(0).setPreferredWidth(70);      
      tabla.getColumnModel().getColumn(1).setPreferredWidth(400);
    }
    
    public void RefreshTemas()
    {
       // La ventana de los temas esta cerrada / no filtrar 
       if (dlgAreas == null)
       {
           return;
       }
       // Nuevo filtro           
       int idx;
       int registroActivo = tabla.getSelectedRow();
       idx = tabla.convertRowIndexToModel(registroActivo); 
       int egel = (int)((ModeloTabla)tabla.getModel()).getValueAt(idx, 0);
       // Rotulamos la ventana del área de Egel
       dlgAreas.setTitle("Areas de conocimiento del "+
                         ((ModeloTabla)tabla.getModel()).getValueAt(idx, 1));
       dlgAreas.RefreshTemas(egel);
    }
