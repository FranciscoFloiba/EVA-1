    private void BotonEgelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEgelActionPerformed
        // Primeramente vemos si está seleccionado un registro
        int registroActivo = tabla.getSelectedRow();
        if (registroActivo == -1)
        {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ningún Egel",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
            return;
        }      
        
        // Obtenemos el id de la asignatura
        int idx;
        registroActivo = tabla.getSelectedRow();
        idx = tabla.convertRowIndexToModel(registroActivo);
        
        // Cerramos la ventana de los temas si ya estaba abierta
        if (dlgAreas != null)
        {
           dlgAreas.dispose();
           dlgAreas = null;
        }
        
        // Ahora tenemos que traer solamente los temas del egel (forzamos filtro)
        int egel = (int)((ModeloTabla)tabla.getModel()).getValueAt(idx, 0);
        dlgAreas = new Egel_Areas(egel);
        // Se agrega la ventana al escritorio
        MDIApplication.escritorio.add(dlgAreas); 
        // Establecemos ubicación de la ventana
        dlgAreas.setLocation(MDIApplication.escritorio.getAllFrames().length*20, MDIApplication.escritorio.getAllFrames().length*20);
        // Rotulamos la ventana del área de Egel
        dlgAreas.setTitle("Areas de conocimiento del "+
                          ((ModeloTabla)tabla.getModel()).getValueAt(idx, 1));
        dlgAreas.setVisible(true); 
        // Alineamos las dos ventanas
        MDIApplication.alignWindowsActionPerformed(evt);
    }//GEN-LAST:event_BotonEgelActionPerformed

