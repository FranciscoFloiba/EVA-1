    private void temasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_temasActionPerformed
        // Primeramente vemos si est√° seleccionado un registro
        int registroActivo = tabla.getSelectedRow();
        if (registroActivo == -1)
        {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ninguna Asignatura",
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
            return;
        }      
        
        // Obtenemos el id de la asignatura
        int idx;
        registroActivo = tabla.getSelectedRow();
        idx = tabla.convertRowIndexToModel(registroActivo);
        
        // Cerramos la ventana de los temas si ya estaba abierta
        if (dlgTemas != null)
        {
           dlgTemas.dispose();
           dlgTemas = null;
        }
        
        // Ahora tenemos que traer solamente los temas de la asignatura (forzamos filtro)
        int materia = (int)((ModeloTabla)tabla.getModel()).getValueAt(idx, 0);
        dlgTemas = new Asignaturas_Temas(materia);
        
        // Se agrega la ventana al escritorio
        MDIApplication.escritorio.add(dlgTemas);      
        // dlgTemas.toFront();
        // Se ubica la ventana en cascada
        dlgTemas.setLocation(MDIApplication.escritorio.getAllFrames().length*20, MDIApplication.escritorio.getAllFrames().length*20);
        /* dlgTemas.repaint();
        try {
            dlgTemas.setMaximum(true);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(MDIApplication.class.getName()).log(Level.SEVERE, null, ex);
        }*/
        dlgTemas.setVisible(true); 
        MDIApplication.alignWindowsActionPerformed(evt);
    }//GEN-LAST:event_temasActionPerformed

    private void tablaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaKeyReleased
        int code = evt.getKeyCode();
        if (code == KeyEvent.VK_DOWN) 
        {
           // Nuevo filtro 
           RefreshTemas();
        }
        if (code == KeyEvent.VK_UP) 
        {
           // Nuevo filtro 
           RefreshTemas();
        }
    }//GEN-LAST:event_tablaKeyReleased

